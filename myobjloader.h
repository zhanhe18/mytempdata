#include <vector>
#include <string>
#include <sstream>
#include <fstream>
bool loadOBJ(const char* path,std::vector<float>& vertex) {
  std::ifstream infile(path);
  std::string line;
  while (std::getline(infile, line)) {
    std::istringstream iss(line);
	std::string ch;
	float x,y,z;
	iss >> ch;
	if (ch != "v")  continue;
	iss >> x >> y >> z;
	vertex.push_back(x);
	vertex.push_back(y);
	vertex.push_back(z);
  }
  return true;
}

